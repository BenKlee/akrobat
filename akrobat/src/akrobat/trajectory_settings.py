class TrajectorySettings():

    def __init__(self, amplitude_width = 0, amplitude_height = 0, max_ticks = 0, number_of_case_steps = 0) -> None:
        self.amplitude_width = amplitude_width
        self.amplitude_height = amplitude_height
        self.max_ticks = max_ticks
        self.number_of_case_steps = number_of_case_steps